import { readFileSync, readdirSync, existsSync, writeFileSync } from 'node:fs';
import { resolve, dirname, join, relative, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const INDEX_PATH = join(ROOT, 'docs', 'index.md');

function findMarkdownFiles(dir, { skipReadme = false } = {}) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir, { recursive: true, withFileTypes: true })
    .filter((entry) => {
      if (!entry.isFile() || !entry.name.endsWith('.md')) return false;
      if (skipReadme && entry.name.toLowerCase() === 'readme.md') return false;
      return true;
    })
    .map((entry) => join(entry.parentPath ?? entry.path, entry.name))
    .sort();
}

function toTitle(filename) {
  return basename(filename, '.md')
    .replace(/-/g, ' ')
    .replace(/\b\w/g, (c) => c.toUpperCase());
}

function buildSection(heading, files, fromDir) {
  if (files.length === 0) return '';
  const lines = [`## ${heading}`, ''];
  for (const filePath of files) {
    const rel = relative(fromDir, filePath).replace(/\\/g, '/');
    const title = toTitle(filePath);
    lines.push(`- [${title}](${rel})`);
  }
  lines.push('');
  return lines.join('\n');
}

const docsDir = join(ROOT, 'docs');
const docsFiles = findMarkdownFiles(docsDir).filter(
  (f) => basename(f) !== 'index.md'
);
const governanceFiles = findMarkdownFiles(join(ROOT, 'governance'), { skipReadme: false });
const evalsFiles = findMarkdownFiles(join(ROOT, 'evals'), { skipReadme: false });
const checklistsFiles = findMarkdownFiles(join(ROOT, 'checklists'), { skipReadme: false });

const indexDir = docsDir;

const content = [
  '# Documentation Index',
  '',
  'Auto-generated index of key documentation in this repository.',
  '',
  buildSection('Core Documentation', docsFiles, indexDir),
  buildSection('Governance', governanceFiles, indexDir),
  buildSection('Evaluation Framework', evalsFiles, indexDir),
  buildSection('Security Checklists', checklistsFiles, indexDir),
  '<!-- Generated by scripts/generate-doc-index.mjs -->',
  '',
].join('\n');

writeFileSync(INDEX_PATH, content, 'utf8');
console.log(`✓ docs/index.md written (${docsFiles.length + governanceFiles.length + evalsFiles.length + checklistsFiles.length} files indexed).`);
